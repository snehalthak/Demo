package com.promptprogram;

public class RealtionalOperators {

	public static void main(String[] args) {
		int a = 20;
		int b = 30;
		
		System.out.println(a & b);  // out as per AND truth table value 01010 like that 
		System.out.println(a | b);  // out as per OR truth table value 01010 like that
	}

}
