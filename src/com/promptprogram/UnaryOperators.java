package com.promptprogram;

public class UnaryOperators {

	public static void main(String[] args) {
			
		int a = 20;
		int b = 30;
		System.out.println(a++);  		// Output : 20
		System.out.println(a);			// Output : 21
		System.out.println(++b); 		// Output : 31
		System.out.println(++b); 		// Output : 32
		System.out.println(b++); 		// Output : 32
		System.out.println(a++); 		// Output : 21
		System.out.println(++b); 		// Output : 33
		System.out.println(a); 			// Output : 22
		System.out.println(a++); 		// Output : 22
		System.out.println(a); 			// Output : 23
		System.out.println(a--); 		// Output : 23
		System.out.println(a); 			// Output : 22
		System.out.println(--b); 		// Output : 33
		System.out.println(--b); 		// Output : 32
		
		
		
		
	}

}
